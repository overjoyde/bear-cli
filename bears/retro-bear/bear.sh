printf "%b\n" "${G}    _ .-""""-._
  .'  _     _  '.
 /   (o)   (o)   \\\
|                 |
|  \           /  |
 \  '.       .'  /
  '.  `'---'`  .'
    '-._____.-'

   .---------------.
  /  .-----------.  \\\
 |  |  >_  >_    |  |
  \  '-----------'  /
   '---------------'${R}"

exit 0
